/**
 * add - adds 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: a + b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtarcts 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies 2 number
 * @a: 1st number
 * @b: 2nd number
 * Return: a * b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - preforms the % operation
 * @a: 1st number
 * @b: 2nd number
 * Return: a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
